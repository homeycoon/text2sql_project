import re
from typing import Optional


def extract_sql(text: str) -> Optional[str]:
    """
    Извлекает SQL запрос из строки, обрамленный тройными кавычками с префиксом 'sql'.

    Ищет первое вхождение подстроки вида '''sql ... ''' и возвращает содержимое между ними.

    Аргументы:
        text: строка, содержащая SQL запрос в указанном формате.

    Возвращает:
        Извлеченный SQL запрос с удаленными начальными и конечными пробелами,
        или None, если вхождение не найдено.
    """
    # Шаблон: открывающие тройные кавычки + 'sql', затем любые пробелы,
    # затем минимальное количество любых символов (включая переводы строк) до первых закрывающих тройных кавычек.
    pattern = r"```sql\s*(.*?)```"
    match = re.search(pattern, text, re.DOTALL | re.IGNORECASE)
    if match:
        return match.group(1).strip()
    return None
